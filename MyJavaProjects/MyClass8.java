class MyClass8 {

	static float sumAverage(int arg[]) {

		float sum=0;
		for(int i = 0; i < arg.length; i++) {
			sum += arg[i];

		}
		return sum / arg.length;

	}

	public static void main(String... args) {

		int arr[] = {5, 9, 34, 64, 3, 0, 456};
		System.out.println(sumAverage(arr));




	}
}
